package DataMM;

public class ChiNhanh {
	private String MaCN;
	private String DiaChi;
	
	public ChiNhanh() {
		
	}

	public ChiNhanh(String maCN, String diaChi) {
		super();
		MaCN = maCN;
		DiaChi = diaChi;
	}

	public String getMaCN() {
		return MaCN;
	}

	public void setMaCN(String maCN) {
		MaCN = maCN;
	}

	public String getDiaChi() {
		return DiaChi;
	}

	public void setDiaChi(String diaChi) {
		DiaChi = diaChi;
	}
	
	
}
